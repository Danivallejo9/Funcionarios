    private void listFuncionarios() throws SQLException {
        cbxFuncionarios.removeAllItems();
        Funcionarios funcionarios1 = new Funcionarios();
        funcionarios1.setNombre(SELECCIONE);
        funcionarios1.setIddoc(WIDTH);
        cbxFuncionarios.addItem(funcionarios1);
        tblFuncionarios.removeAll();

        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }

        tblFuncionarios.setModel(defaultTableModel);
        try {
            List<Funcionarios> funcionario = funcionariosController.obtenerFuncionarios();
            if (funcionario.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(funcionario.size());
            int row = 0;
            for (Funcionarios funcionarios : funcionario) {
                defaultTableModel.setValueAt(funcionarios.getIddoc(), row, 0);
                defaultTableModel.setValueAt(funcionarios.getTipodoc(), row, 1);
                defaultTableModel.setValueAt(funcionarios.getNombre(), row, 2);
                defaultTableModel.setValueAt(funcionarios.getApellidos(), row, 3);
                defaultTableModel.setValueAt(funcionarios.getEstadocivil(), row, 4);
                defaultTableModel.setValueAt(funcionarios.getSexo(), row, 5);
                defaultTableModel.setValueAt(funcionarios.getDireccion(), row, 6);
                defaultTableModel.setValueAt(funcionarios.getTelefono(), row, 7);
                defaultTableModel.setValueAt(funcionarios.getFechanac(), row, 8);
                row++;

                cbxFuncionarios.addItem(funcionarios);

            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void addListener() {
        cbxFuncionarios.addItemListener(event -> {
            Funcionarios selectedFuncionario = (Funcionarios) event.getItem();
            if (selectedFuncionario.getNombre().equals(SELECCIONE)) {
                txtIdDoc.setText("");
                txtTipoDoc.setText("");
                txtNombre.setText("");
                txtApellidos.setText("");
                txtEstadoCivil.setText("");
                txtSexo.setText("");
                txtDireccion.setText("");
                txtTelefono.setText("");
                txtFechaNac.setText("");

            } else {
                txtIdDocEdit.setText(String.valueOf(selectedFuncionario.getIddoc()));
                    txtTipoDocEdit.setText(selectedFuncionario.getTipodoc());
                    txtNombreEdit.setText(selectedFuncionario.getNombre());
                    txtApellidosEdit.setText(selectedFuncionario.getApellidos());
                    txtEstadoCivilEdit.setText(selectedFuncionario.getEstadocivil());
                    txtSexoEdit.setText(selectedFuncionario.getSexo());
                    txtDireccionEdit.setText(selectedFuncionario.getDireccion());
                    txtTelefonoEdit.setText(selectedFuncionario.getTelefono());
                    txtFechaNacEdit.setText(String.valueOf(selectedFuncionario.getFechanac()));
            }
            System.out.println("selectedFuncionario = " + selectedFuncionario);
        });
    }
    
