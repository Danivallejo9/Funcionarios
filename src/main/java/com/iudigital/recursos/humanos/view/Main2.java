    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (txtIdDoc.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Id");
            txtIdDoc.requestFocus();
            return;
        }

        if (txtTipoDoc.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite tipo de documento");
            txtTipoDoc.requestFocus();
            return;
        }

        if (txtNombre.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Nombre");
            txtNombre.requestFocus();
            return;
        }

        if (txtApellidos.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Apellidos");
            txtApellidos.requestFocus();
            return;
        }

        if (txtEstadoCivil.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Estado Civil");
            txtEstadoCivil.requestFocus();
            return;
        }

        if (txtSexo.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite sexo");
            txtSexo.requestFocus();
            return;
        }

        if (txtDireccion.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite dirección");
            txtDireccion.requestFocus();
            return;
        }

        if (txtTelefono.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite teléfono");
            txtTelefono.requestFocus();
            return;
        }

        if (txtFechaNac.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite fecha de nacimiento");
            txtFechaNac.requestFocus();
            return;
        }

        Funcionarios funcionario = new Funcionarios();
        funcionario.setIddoc(Integer.parseInt (txtIdDoc.getText().trim()));
        funcionario.setTipodoc(txtTipoDoc.getText().trim());
        funcionario.setNombre(txtNombre.getText().trim());
        funcionario.setApellidos(txtApellidos.getText().trim());
        funcionario.setEstadocivil(txtEstadoCivil.getText().trim());
        funcionario.setSexo(txtSexo.getText().trim());
        funcionario.setDireccion(txtDireccion.getText().trim());
        funcionario.setTelefono(txtTelefono.getText().trim());
        funcionario.setFechanac(Date.valueOf (txtFechaNac.getText().trim()));

        try {
            funcionariosController.crearFuncionario(funcionario);
            txtIdDoc.setText("");
            txtTipoDoc.setText("");
            txtNombre.setText("");
            txtApellidos.setText("");
            txtEstadoCivil.setText("");
            txtSexo.setText("");
            txtDireccion.setText("");
            txtTelefono.setText("");
            txtFechaNac.setText("");
            listFuncionarios();
            JOptionPane.showMessageDialog(null, "Funcionario creado con éxito");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Funcionario no pudo ser creado");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

