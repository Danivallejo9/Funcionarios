    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        if (txtIdDocEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar el funcionario de la lista");
            txtIdDocEdit.requestFocus();
            return;
        }
        
        int opt = JOptionPane.showConfirmDialog(null, "Está seguro de eliminar el funcionario","Confirmar salida",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (opt == 0) {
            try {
                funcionariosController.eliminarFuncionario(Integer.parseInt(txtIdDocEdit.getText()));
                LimpiarTxtEdit();
                listFuncionarios();
                
                JOptionPane.showMessageDialog(null, "Se ha eliminado con éxito");
            }catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "No fue posible eliminar funcionario");
            }
        }        
    }//GEN-LAST:event_btnEliminarActionPerformed
       
